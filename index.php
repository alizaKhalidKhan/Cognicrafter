                        <h1 class="mb-4">About Us</h1>
                        <p>CogniCrafter is a user-focused platform dedicated to simplifying the daily lives of individuals and caregivers dealing with Alzheimer's 
                            and related challenges. Our mission is to provide comfort and assistance on this often challenging journey by
                             offering a range of essential features and support.</p>
                
                             <h6>Pill Reminders:</h6>
                             <p>CogniCrafter offers an easy-to-use pill reminder feature 
                                to ensure that medication is taken on time. We understand the importance of timely 
                                medication in managing Alzheimer's, and our tool helps ease the burden of keeping track 
                                of multiple medications.</p>

                             <h6>Sticky Notes:</h6>
                             <p>Our platform includes digital sticky notes that can be used to leave 
                                important reminders or notes for both caregivers and patients. 
                                This feature aids in communication and memory support.</p>

                             <h6>Routine Activities:</h6>
                             <p>We recognize the significance of maintaining a structured routine for individuals with Alzheimer's. 
                                CogniCrafter assists in planning and tracking daily activities, providing stability and a sense of normalcy.</p>
                            
                             <h6>Relative Information:</h6>
                             <p>CogniCrafter offers a secure space for storing and accessing essential information about loved ones.</p>

                        <p class="mb-4">At CogniCrafter, we prioritize the well-being of individuals and
                             their caregivers by providing a user-friendly, comprehensive, and supportive platform. 
                             Our goal is to make the journey of dealing with Alzheimer's more manageable and less stressful, 
                             offering the peace of mind that comes with having access to the right tools and resources. 
                             We are committed to enhancing the quality of life for both patients and their caregivers 
                             as they navigate this challenging path.</p>
